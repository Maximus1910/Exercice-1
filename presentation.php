		<h1>DIGITAL BUSINESS SCHOOL (DBS)</h1>
			<img src="Batiment.jpg" class="Batiment" width="100%" height="85%"/>
			<div class="para">
				<p class="para1">
					Face à l'avènement du numérique, le digital est devenu une activité incontournable. C'est dans ce contexte que la Digital Business School voit le jour. Néanmoins, les entreprises du secteur des médias peinent à trouver des profils en phase avec leurs besoins. <br>
					Pour répondre à cette carence, DBS se positionne comme la première école digitale au Gabon spécialisée dans les métiers du web.
					Forte d'un réseau conséquent d'intervenants prestigieux du domaine des médias, nos enseignants sont essentiellement des professionnels du monde du web et du design qui sont à la tête d'entreprises et dont les expertises ont été éprouvées. <br> 
					En plus de cela, DBS vous propose, un cursus de type Bachelor ou Master qui au sortir de là vous rendra directement employable sur le marché du travail. <br>
					Nous nous engageons auprès de nos étudiants à les former en 3 ans conformément aux besoins des entreprises et du marché Gabonais et Africain aux métiers du Web en les préparant à une large gamme de métiers connexes, dans le domaine de :
				</p>
						<ul class="info"> 
							<li class="info-sup">Communication digitale</li>
							<li class="info-sup">Développement web</li>
							<li class="info-sup">E-commerce</li>
							<li class="info-sup">Infographie/multimédia </li>
						</ul>
				<p class="para1">
					<b>VISION</b> : Construire des générations d'experts dans les nouveaux métiers du Web & du Digital et Construire les Digital Entrepreneurs d'aujourd'hui.<br>
					<b>MISSION</b> : Former les étudiants aux nouveaux métiers du digital en leur apportant des cours de qualité en phase avec l'évolution des nouvelles technologies de notre temps et des problématiques rencontrées par les entreprises. <br>
					<b>VALEURS</b> : Excellence, Dynamisme, Passion, Discipline, Créativité.
				</p>
